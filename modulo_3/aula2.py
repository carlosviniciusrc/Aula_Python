# Função Print()

#É a função utilizada para exibir algo na tela. E sempre vai receber um argumento.
print(123)



# Caso queira usar mais de um argumento você utiliza a vírgula.
print(12, 34)
print(56, 78)

# Como aplicar outro separador, SEP significa separador

print(12 ,34, sep='-')
print(12 ,34, sep="-")

# Argumento End para pular linhas, ou adicionar algum caractere.

print(12 ,34, sep='-', end='#\n\n')
print(56, 78, sep='-', end='@')



