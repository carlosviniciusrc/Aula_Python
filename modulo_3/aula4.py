# Tipos de int e float


# int -> Número inteiro
# Positivo ou negativo. Int sem sinal é considerado
# positivo.
print(11) # int
print(-11) # int 
print(0)


# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto fluante.
# float sem sinal é considerado positivo.
print(1.1) #float
print(1.1, 10.11) #float
print(-1.1, -10.11, end="\n\n\n") #float

# A função type mostra o tipo que o Python 
# inferiu ao valor.

print(type('Vinicius'))
print(type(0))
print(type(1.1), type(-2), type(-1.3))

