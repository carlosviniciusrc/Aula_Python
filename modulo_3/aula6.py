# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos Imutáveis e primitivos:
# str, int, float, bool

# type convertion:

print(int('1'), type(int('1'))) # convertendo um str para int.
print(int('1') + 1) # conversão e fazendo soma

# Quando você soma float com int, o resultado é float.
print(float('1') + 1) # conversao para float.

# Bool

print(bool('')) # String sem espaço o valor é false
print(bool(' ')) # String com espaço o valor é true


# Str
print(str(11) + 'b') # conversao para string.
