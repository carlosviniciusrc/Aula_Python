# Tipo de dado bool(boolean)
# Ao questionar algo em um programa
# #só existem duas respostas possiveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que 
# questiona se um valor é igual a outro.
print(10 == 10) # Sim -> True (Verdadeiro)  
print(10 == 11, end = '\n\n\n') # Não -> False (Falso)
# Vai mostrar o tipo de dado com a função 'type':
print(type(10 == 10))
print(type(10 == 11))
# O == é diferente de =. O == vai questionar se as duas coisas são iguais, 
# já o = vai atribuir valor a variavel.