# Concatenação (+) e repetição (*) com operadores aritméticos

concatenacao = 'A' + 'B' + 'C' # Em caso de strings o sinal de + vai fazer a junção
concatenacao2 = 'Vinixs' + str(0) + str(4)

print(concatenacao)
print(concatenacao2)

repeticao1 = 'V' * 6 # Nesse caso a STtring vai ser multiplicada
repeticao2 = 'Vini' * 4 

print(repeticao1)
print(repeticao2)